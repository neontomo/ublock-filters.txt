// Remove unneeded stuff
api.unmap('t') // Open a URL
api.unmap('T') // Choose a tab
api.unmap('W') // Move current tab to another window
api.unmap('go') // Open a URL in current tab
api.unmap('U') // Scroll full page up
api.unmap('P') // Scroll full page down
api.unmap('%') // Scroll to percentage of current page
api.unmap(';w') // Focus top window
api.unmap('w') // Switch frames
api.unmap('gT') // Go to first activated tab
api.unmap('gt') // Go to last activated tab
api.unmap('gp') // Go to the playing tab
api.unmap('g?') // Reload current page without query string(all parts after question mark)
api.unmap('g#') // Reload current page without hash fragment
api.unmap('A') // LLM
api.unmap('B') // Go one tab history back (not same as back button)
api.unmap('F') // Go one tab history forward (not same as forward button)

// Yank URL w/ one press (disables other yx binds)
api.map('y', 'yy')

// History Back/Forward + Scroll handling
api.map('w', 'u') // scroll up: w
api.map('s', 'd') // scroll down: s
api.map('a', 'S') // back: a
api.map('d', 'D') // forward: d
api.unmap('e') // remove extra scroll up

